#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;

	if (!dstsize)
		return (dstsize + ft_strlen(src));
	i = 0;
	j = 0;
	while (dst[i])
		i++;
	while (src[j] && i + j < dstsize - 1)
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	if (i > dstsize)
		return (dstsize + ft_strlen(src));
	return (i + ft_strlen(src));
}
